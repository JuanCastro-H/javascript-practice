/* 
* Realizar en Javascript la cuenta regresiva para el año nuevo,
comenzar en 10 y terminar en 0.

* Al finalizar, debe mostrarse el mensaje "Feliz año nuevo".

*/
alert("hola")

for(var contador = 10 ; contador >= 1 ; contador--){

    console.log("El año nuevo comienz en: " + contador);

    if (contador == 1){
        alert("Feliz año nuevo");
    }
}